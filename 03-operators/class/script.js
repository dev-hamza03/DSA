
// =====================
// Arithmetic Operators
// =====================
let a = 10;
let b = 5;

console.log(a + b); // 15
console.log(a - b); // 5
console.log(a * b); // 50
console.log(a / b); // 2
console.log(a % b); // 0
console.log(a ** b); // 100000





// =====================
// Relational Operators
// =====================
let x = 10;
let y = 20;

console.log("\nRelational:");
console.log(x > y);   // false
console.log(x < y);   // true
console.log(x >= 10); // true
console.log(x <= 5);  // false

console.log(x == "10");  // true
console.log(x === "10"); // false
console.log(x != y);     // true
console.log(x !== "10"); // true





